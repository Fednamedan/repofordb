-- Проверяем количество записей в основных таблицах
SELECT 
    'Countries' as table_name, COUNT(*) as record_count FROM Countries
UNION ALL SELECT 'Cities', COUNT(*) FROM Cities
UNION ALL SELECT 'Faculties', COUNT(*) FROM Faculties
UNION ALL SELECT 'Departments', COUNT(*) FROM Departments
UNION ALL SELECT 'Professors', COUNT(*) FROM Professors
UNION ALL SELECT 'Students', COUNT(*) FROM Students
UNION ALL SELECT 'Courses', COUNT(*) FROM Courses
UNION ALL SELECT 'Grades', COUNT(*) FROM Grades
UNION ALL SELECT 'Publications', COUNT(*) FROM Publications
UNION ALL SELECT 'ResourceLoans', COUNT(*) FROM ResourceLoans;